# iterate over a number
for i in range(10):
    print("Count", i)

# iterate in range
for i in range(1,11):
    print("Count", i)

# iterate on characters of string
str = "Hello World"
for letter in str:
    print(letter)

# iterate over list
listA = ['sahitya', 'anmol', 'satyam', 'kumar']
for item in listA:
    print(item)

# iterate over a tuple
tupleA = ('sahitya', 'anmol', 'satyam', 'shubhangi')
for tupleItem in tupleA:
    print(tupleItem)