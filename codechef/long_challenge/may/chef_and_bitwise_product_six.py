def correct_value(x, y, l, r):
    max_product = 0
    max_product_z = l
    for i in range(l, r + 1):
        z = (x & i) * (y & i)
        if z > max_product:
            max_product = z
            max_product_z = i
    return max_product_z


def get_z(x, y, l, r):
    choices = [l, r]
    alterable_bits = len(bin(l ^ r)) - 3
    max_possible_z = x | y

    max_product = -1
    max_z = 0

    set_bit = 2 ** alterable_bits
    mask = set_bit - 1
    for i in range(alterable_bits):
        set_bit = set_bit >> 1
        mask = mask >> 1
        masked_or = mask & max_possible_z
        if l & set_bit == 0 and max_possible_z & set_bit > 0:
            z = ((l | set_bit) ^ (l & mask)) | masked_or
            choices.append(z)

    set_bit = 2 ** alterable_bits
    mask = set_bit - 1
    for i in range(alterable_bits):
        set_bit = set_bit >> 1
        mask = mask >> 1
        masked_or = mask & max_possible_z
        if r & set_bit > 0:
            z = (r ^ (r & ((set_bit << 1) - 1))) | masked_or
            choices.append(z)

    # check:
    for z in choices:
        product = (x & z) * (y & z)
        if product > max_product:
            max_product = product
            max_possible_z = z
        elif product == max_product and z < max_possible_z:
            max_possible_z = z
    # print('choices', choices)
    return max_possible_z


