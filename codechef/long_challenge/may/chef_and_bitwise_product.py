def get_max(x, y, l, r):
    maxv = 0
    maxi = 0
    for i in range(l, r + 1):
        nmax = (x & i) * (y & i)
        if nmax > maxv:
            maxv = nmax
            maxi = i
    return maxi


def get_max_z(x, y, l, r, powers, sum, index):
    if sum > r:
        return 0
    if index == len(powers):
        if sum < l:
            return 0
        return sum
    p = get_max_z(x, y, l, r, powers, sum + 2 ** powers[index], index + 1)
    q = get_max_z(x, y, l, r, powers, sum, index + 1)
    ap = (p & x) * (p & y)
    aq = (q & x) * (q & y)
    # print(index, p, q, ap, aq)
    if ap == aq:
        return min(p, q)
    if ap < aq:
        return q
    return p


def get_z(x, y, l, r):
    exp_z = x | y
    if r > exp_z:
        return exp_z
    if l > exp_z:
        return 0
    output = [int(k) for k in '{:b}'.format(exp_z)]
    powers = []
    p = 0
    for i in range(len(output) - 1, -1, -1):
        if output[i] == 1:
            powers.append(p)
        p += 1
    return get_max_z(x, y, l, r, powers, 0, 0)


